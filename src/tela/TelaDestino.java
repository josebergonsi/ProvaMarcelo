    private void carregaTable(List<Destino> clienteList) {
        if (clienteList == null) {
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tableDestino.getModel();
        model.setRowCount(0);
        for (Destino c : clienteList) {
            model.addRow(new Object[]{c.getCodigo_destino(), c.getDescricao_destino(), c.getData_ini(), c.getData_fim(), c.getVltotal_dest()});
        }
    }

 


