    private void novo() {
        try {
            tfcodigods.setText(String.valueOf(destinoDAO.getLastId()));
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        desabilitaCampos(true);
        btSalvar.setEnabled(true);
    }

   public Boolean validaCampos() {

        if (tfcodigods.getText().trim().length() <= 0) {
            JOptionPane.showMessageDialog(null, "Informe um código para salvar.");
            return false;
        } else if (tfdescricaods.getText().trim().length() <= 0) {
            JOptionPane.showMessageDialog(null, "Informe um Destino para salvar.");
            return false;
        } else if (tfdatainicio.getText().trim().length() <= 0) {
            JOptionPane.showMessageDialog(null, "Informe uma Data Inicio para salvar.");
            return false;
        } else if (tfdatafim.getText().trim().length() <= 0) {
            JOptionPane.showMessageDialog(null, "Informe uma Data fim para salvar.");
            return false;
        } else if (tfvalortotal.getText().trim().length() <= 0) {
            JOptionPane.showMessageDialog(null, "Informe um Valor para salvar.");
            return false;
        }

        return true;
    }
