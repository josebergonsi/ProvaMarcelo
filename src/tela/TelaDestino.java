       private void salvar() {

        try {
          
            Destino destino = new Destino();
            destino.setCodigo_destino(Integer.parseInt(tfcodigods.getText()));
            destino.setDescricao_destino(tfdescricaods.getText());
            destino.setData_ini(java.sql.Date.valueOf(tfdatainicio.getText()));
            destino.setData_fim(java.sql.Date.valueOf(tfdatafim.getText()));
            destino.setVltotal_dest(Float.parseFloat(tfvalortotal.getText()));

            try {
limpaCampos();
                destinoDAO.save(destino);
                JOptionPane.showMessageDialog(null, "Destino Salvo com Sucesso");
              
                carregaTable(destinoDAO.getAll());
            } catch (SQLException ex) {
                ex.printStackTrace();
            }

        } catch (NumberFormatException nfe) {
            JOptionPane.showMessageDialog(null, "Caracter Inválido");
        }
    }

    private void desabilitaCampos(boolean ativo) {
        tfcodigods.setEnabled(ativo);
        tfdescricaods.setEnabled(ativo);
        tfdatainicio.setEnabled(ativo);
        tfdatafim.setEnabled(ativo);
        tfvalortotal.setEnabled(ativo);
    }

    private void limpaCampos() {
        tfcodigods.setText("");
        tfdescricaods.setText("");
        tfdatainicio.setText("");
        tfdatafim.setText("");
        tfvalortotal.setText("");
    }

    private void filtrar() {
        try {
            if (rbcodigo.isSelected() && tfcodigoft.getText().trim().length() > 0) {
                Destino cliente = destinoDAO.getById(Integer.parseInt(tfcodigoft.getText()));
                List<Destino> clienteList = new ArrayList<>();
                clienteList.add(cliente);
                carregaTable(clienteList);
            } else if (rbdescricao.isSelected() && tfdescricaoft.getText().trim().length() > 0) {
                carregaTable(destinoDAO.getByName(tfdescricaoft.getText()));
            } else {
                JOptionPane.showMessageDialog(null, "Informe qual filtro desejado para pesquisa");
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    private void remover() {
        int linhaSelecionada = tableDestino.getSelectedRow();
        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(null, "Deve-se selecionar uma linha para ser removido");
            return;
        }
        int codigoRemover = (int) tableDestino.getValueAt(linhaSelecionada, 0);
        try {
            destinoDAO.delete(codigoRemover);
            JOptionPane.showMessageDialog(null, "Destino Removido com Sucesso!");
            carregaTable(destinoDAO.getAll());
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }

